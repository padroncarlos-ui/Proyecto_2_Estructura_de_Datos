    /**
     * Recupera la lista de usuarios del sistema y los añade a una ComboBox.
     * Permite que el usuario seleccione al propietario del nuevo documento.
     */
    private void llenarComboUsuarios() {       
        Lista<Usuario> usuarios = sistema.getListaUsuarios();

        for (int i = 0; i < usuarios.getTamaño(); i++) {
            Usuario u = usuarios.obtener(i);
            if (u != null) {
                ComboUsuarios.addItem(u.getNombre()); 
            }
        }
    }
    
