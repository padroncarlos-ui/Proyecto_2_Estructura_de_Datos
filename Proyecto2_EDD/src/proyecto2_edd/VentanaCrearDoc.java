    /**
     * Procesa la creación del documento tras validar los campos de texto.
     * Realiza la conversión de tipos y vincula el objeto Documentos al Usuario correspondiente.
     */
    private void BtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearActionPerformed
        
        Object seleccionUsuario = ComboUsuarios.getSelectedItem();
        String nombreDoc = NombreDoc.getText().trim();
        String tamanoStr = TamanoDoc.getText().trim();
        String tipoDoc = TipoDoc.getText().trim();
     
        boolean esPrioritario = ComboPrioridad.getSelectedItem().toString().equals("Si");
        //Validaciones.
        if (seleccionUsuario == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un usuario.");
            return;
        }

        String nombreUser = seleccionUsuario.toString();

        if (nombreDoc.isEmpty() || tamanoStr.isEmpty() || tipoDoc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }
        try {
            int tamano = Integer.parseInt(tamanoStr); //Conversión del tamaño a entero.       
            Usuario user = (Usuario) sistema.getTablaUsuarios().buscar(nombreUser); //Búsqueda O(1) del usuario en la Tabla Hash.       

            if (user != null) {            
                Documentos nuevoDoc = new Documentos(nombreDoc, tamano, tipoDoc); //Creación y configuración del objeto Documento.
                nuevoDoc.setPrioritario(esPrioritario); //Se guarda la prioridad para el futuro.
                user.agregarDocumento(nuevoDoc); //El usuario guarda el documento en su lista interna.

                String mensaje = "Documento '" + nombreDoc + "' creado para " + nombreUser;
                if (esPrioritario) mensaje += " (Marcado como Prioritario)";
                //Actualización del mensaje creado en la interfaz.
                principal.setMensajeProceso(mensaje);
                principal.ListaDeUsuariosInfo(); 

                this.dispose(); 
            } else {
                JOptionPane.showMessageDialog(this, "Error: Usuario no encontrado en el sistema.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El tamaño debe ser un número entero válido.");
        }
    }//GEN-LAST:event_BtnCrearActionPerformed

