    /**
     * Evento principal de eliminación. Identifica al dueño y al documento, 
     * procede a borrarlo y actualiza todas las estructuras.
     */
    private void BtnEliminarDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarDocActionPerformed

        String seleccion = (String) ComboElimDoc.getSelectedItem();
        if (seleccion == null){
            String mensaje = "No hay documentos para eliminar.";
            principal.setMensajeProceso(mensaje);
            this.dispose();
            return;
        }
        
        String[] partes = seleccion.split(": "); //Parseo del string "Usuario: Documento"
        String nombreUser = partes[0];
        String nombreDoc = partes[1];
        Usuario user = (Usuario) sistema.getTablaUsuarios().buscar(nombreUser);

        if (user != null) {
            Documentos docAEliminar = null;
            Lista<Documentos> listaD = user.getDocumentos();
            for (int i = 0; i < listaD.getTamaño(); i++) {
                if (listaD.obtener(i).getNombre().equals(nombreDoc)) {
                    docAEliminar = listaD.obtener(i);
                    break;
                }
            }
            if (docAEliminar != null) {
                //Si el documento estaba en la cola, aplicamos lógica de eliminación en Heap.
                if (docAEliminar.isEnCola()) {
                    eliminarDeLaCola(docAEliminar);
                }
                user.eliminarDocumento(docAEliminar);
            }
        } 
        else {
            
            MinHeap heap = sistema.getColaImpresion(); //Manejo de eliminación para documentos huérfanos en la cola.
            RegistroImpresion[] registros = heap.getHeap();
            for (int i = 0; i < heap.getTamaño(); i++) {
                Documentos d = registros[i].getDocumento();
                if (registros[i].getNombreUsuario().equals(nombreUser) && d.getNombre().equals(nombreDoc)) {
                    eliminarDeLaCola(d);
                    break;
                }
            }
        }

        String exito = "Documento eliminado con éxito.";
        principal.setMensajeProceso(exito);
        principal.ListaDeUsuariosInfo();
        principal.ColaInfo();
        this.dispose();
    }//GEN-LAST:event_BtnEliminarDocActionPerformed

    /**
     * Técnica especial para eliminar un nodo específico de un MinHeap.
     * 1. Se le asigna una prioridad máxima (valor -1) para que suba a la raíz.
     * 2. Se usa 'flotar' para moverlo a la posición 0.
     * 3. Se ejecuta eliminarMin() para remover la raíz actual.
     * @param doc Referencia del documento a remover del heap.
     */
    private void eliminarDeLaCola(Documentos doc) {
        MinHeap heap = sistema.getColaImpresion();
        RegistroImpresion[] arreglo = heap.getHeap();
        int tamaño = heap.getTamaño();

        for (int i = 0; i < tamaño; i++) {
            if (arreglo[i].getDocumento() == doc) {
                arreglo[i].setEtiquetaTiempo(-1); //Forzamos al nodo a ser el mínimo absoluto.                
                heap.flotar(i);
                heap.eliminarMin();
                break;
            }
        }
    }
    
