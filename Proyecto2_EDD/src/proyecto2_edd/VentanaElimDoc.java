    /**
     * Carga el ComboBox con todos los documentos existentes.
     * Incluye una lógica especial para mostrar documentos de usuarios que ya 
     * fueron eliminados pero cuyos archivos siguen en la cola de impresión.
     */
    private void llenarComboDoc() {
        //Recorre los usuarios activos y sus documentos.
        Lista<Usuario> usuarios = sistema.getListaUsuarios();
        for (int i = 0; i < usuarios.getTamaño(); i++) {
            Usuario u = usuarios.obtener(i);
            Lista<Documentos> docs = u.getDocumentos();
            for (int j = 0; j < docs.getTamaño(); j++) {
                Documentos d = docs.obtener(j);
                ComboElimDoc.addItem(u.getNombre() + ": " + d.getNombre());
            }
        }
        //Busca los documentos sin dueño en la cola.
        MinHeap heap = sistema.getColaImpresion();
        RegistroImpresion[] registros = heap.getHeap();
        for (int i = 0; i < heap.getTamaño(); i++) {
            String nombreUserEnCola = registros[i].getNombreUsuario();
            String nombreDocEnCola = registros[i].getDocumento().getNombre();
            //Si el usuario ya no existe en la tabla hash, el documento es huérfano.
            if (sistema.getTablaUsuarios().buscar(nombreUserEnCola) == null) {
                String item = nombreUserEnCola + ": " + nombreDocEnCola;

                boolean existe = false;
                for (int k = 0; k < ComboElimDoc.getItemCount(); k++) {
                    if (ComboElimDoc.getItemAt(k).equals(item)) {
                        existe = true;
                        break;
                    }
                }
                if (!existe) {
                    ComboElimDoc.addItem(item);
                }
            }
        }
    }
