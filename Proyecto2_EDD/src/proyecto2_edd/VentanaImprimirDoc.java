    /**
     * Acción del botón Imprimir.
     * Recupera el documento seleccionado y lo inserta en el MinHeap a través del sistema.
     */
    private void BtnMandarImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnMandarImprimirActionPerformed

        String seleccion = (String) ComboDocumentosCreados.getSelectedItem();
        if (seleccion == null) return;
        
        String[] partes = seleccion.split(": "); //Separar el nombre de usuario del nombre del documento.
        String nombreUsuario = partes[0];
        String nombreDoc = partes[1];

        Usuario user = (Usuario) sistema.getTablaUsuarios().buscar(nombreUsuario); //Búsqueda O(1) del propietario.
        if (user != null) {
            Lista<Documentos> listaD = user.getDocumentos();
            //Localiza el objeto documento dentro de la lista del usuario.
            for (int i = 0; i < listaD.getTamaño(); i++) {
                Documentos d = listaD.obtener(i);

                if (d.getNombre().equals(nombreDoc)) {
                    sistema.enviarACola(d, user, d.isPrioritario()); //Envío al MinHeap con etiqueta de tiempo y prioridad.

                    String msg = d.isPrioritario() ? "Prioridad aplicada" : "Enviado a la cola.";
                    principal.setMensajeProceso(msg);

                    //Actualización de la Interfaz Principal.
                    principal.ListaDeUsuariosInfo(); 
                    principal.ColaInfo(); 
                    this.dispose();
                    break;
                }
            }
        }        
    }//GEN-LAST:event_BtnMandarImprimirActionPerformed

