    /**
     * Recorre todos los usuarios y sus listas de documentos.
     * Solo añade al ComboBox aquellos documentos cuyo estado 'enCola' es false.
     */
    private void llenarComboDoc() {       
        Lista<Usuario> usuarios = sistema.getListaUsuarios();
        
        for (int i = 0; i < usuarios.getTamaño(); i++) {
            Usuario u = usuarios.obtener(i);
            Lista<Documentos> docs = u.getDocumentos();
            
            for (int j = 0; j < docs.getTamaño(); j++) {
                Documentos d = docs.obtener(j);
                //Evita los duplicados en la cola de impresión.
                if (!d.isEnCola()) {
                    ComboDocumentosCreados.addItem(u.getNombre() + ": " + d.getNombre());
                }
            }
        }
    }
 
