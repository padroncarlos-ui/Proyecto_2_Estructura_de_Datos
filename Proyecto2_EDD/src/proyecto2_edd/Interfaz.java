    /**
     * Botón que avanza en la cola de impresión, simulando que el documento
     * que está al principio de la cola se imprimió y la cola avanza
     * @param evt 
     */
    private void BtnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnImprimirActionPerformed
        RegistroImpresion impreso = sistema.liberarImpresora(); //Procesa el documento con mayor prioridad (raíz del Heap)

        if (impreso != null) {
            Documentos doc = impreso.getDocumento();
            String usuario = impreso.getNombreUsuario();
            String mensaje = "Se ha impreso el documento '" + doc.getNombre() + "." + doc.getTipo() + 
                             "' del usuario '" + usuario + "' con éxito.";
            ListaDeImpresos.setText(mensaje);
            ColaInfo(); //Actualiza visualmente la cola.
            ListaDeUsuariosInfo(); //Actualiza el estado de los documentos del usuario.
            
        } else {
            ListaDeImpresos.setText("No hay documentos pendientes en la cola.");
        }
    }//GEN-LAST:event_BtnImprimirActionPerformed

