    /**
     * Botón que al ser presionado abrirá el JFileChooser para escoger el archivo
     * csv con los usuarios para poder cargarlos al sistema.
     * @param evt 
     */
    private void BtnAddUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddUsuarioActionPerformed
        JFileChooser selector = new JFileChooser(); //Abre un selector de archivos para cargar usuarios.
        int resultado = selector.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File f = selector.getSelectedFile();
            Archivo gestorArchivo = new Archivo(this); 

            int cantidadCargada = gestorArchivo.CargarUsuario(f, sistema);

            if (cantidadCargada > 0) {
                Activar(true); //Empieza el sistema tras la primera carga exitosa.
                ListaDeUsuariosInfo();
            } else {

                if (ListaDeImpresos.getText().isEmpty() || ListaDeImpresos.getText().contains("Tiempo")) {
                    ListaDeImpresos.setText("Error: El archivo no tiene formato válido (Usuario, prioridad)");
                }
            }
        }
    }//GEN-LAST:event_BtnAddUsuarioActionPerformed

