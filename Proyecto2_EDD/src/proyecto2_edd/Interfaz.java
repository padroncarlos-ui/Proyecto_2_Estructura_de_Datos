    /**
     * Recorre la lista de usuarios y construye una representación visual de 
     * cada uno junto con sus documentos asociados y sus estados.
     */
    public void ListaDeUsuariosInfo() {
        UsuariosTxt.setText(""); 
        Lista<Usuario> lista = sistema.getListaUsuarios();
    
        if (lista.esVacia()) {
            UsuariosTxt.setText("No hay usuarios registrados.");
            return;
        }

        StringBuilder sb = new StringBuilder();
        sb.append("USUARIOS Y DOCUMENTOS REGISTRADOS\n");

        for (int i = 0; i < lista.getTamaño(); i++) {
            Usuario u = lista.obtener(i);
            sb.append("USUARIO: ").append(u.getNombre()).append(" [").append(u.getTipo()).append("]\n").append("DOCUMENTOS ASOCIADOS:\n");

            
            Lista<Documentos> docs = u.getDocumentos();
            if (docs.esVacia()) {
                sb.append("(Sin documentos creados)\n");
            } else {
                for (int j = 0; j < docs.getTamaño(); j++) {
                    Documentos d = docs.obtener(j);
                    String estado = d.isEnCola() ? "[EN COLA]" : "[PENDIENTE]";
                    sb.append(d.getNombre()).append(".").append(d.getTipo())
                      .append(" - ").append(d.getTamaño()).append(" pág. ").append(estado).append("\n");
                }
            }
            sb.append("------------------------------------------\n");
        }

        UsuariosTxt.setText(sb.toString());
    }
    
    /**
     * Extrae el estado actual del MinHeap en el sistema y lo muestra 
     * en formato de tabla dentro del área de 'Cola de Impresión'.
     */
    public void ColaInfo (){
        ColaTxt.setText("");
        MinHeap heap = sistema.getColaImpresion();
        RegistroImpresion[] datos = heap.getHeap();
        int total = heap.getTamaño();

        if (total == 0) {
            ColaTxt.setText("La cola de impresión está vacía.");
            return;
        }

        StringBuilder sb = new StringBuilder();
        sb.append("ORDEN DE IMPRESIÓN:\n");
        sb.append("----------------------------------------------------------------\n");
        sb.append(String.format("%-10s | %-15s | %-10s | %-10s\n", "TIEMPO", "ARCHIVO", "TAMAÑO", "USUARIO"));
        sb.append("----------------------------------------------------------------\n");


        for (int i = 0; i < total; i++) {
            RegistroImpresion r = datos[i];
            Documentos d = r.getDocumento();

            sb.append(String.format("%-10d | %-15s | %-10d | %-15s\n", 
                      r.getEtiquetaTiempo(), 
                      d.getNombre() + "." + d.getTipo(),
                      d.getTamaño(),
                      r.getNombreUsuario()));
        }

        ColaTxt.setText(sb.toString());
    }

